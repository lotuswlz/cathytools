package com.thoughtworks.cathywu.tools.imagetools;

/**
 * @author lzwu
 * @since 7/2/15
 */
public class Measurements {
    private int width;
    private int height;

    public Measurements(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
